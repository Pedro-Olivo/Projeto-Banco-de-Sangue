public class Paciente {
    
    private String nomePaciente;
    private int idadePaciente;
    private String CPFPaciente;
    private String enderecoPaciente;
    private String telefonePaciente;
    private String tipoSanguineoPaciente;

    
    public String getNomePaciente() {
        return nomePaciente;
    }
    public void setNomePaciente(String nomePaciente) {
        this.nomePaciente = nomePaciente;
    }
    public int getIdadePaciente() {
        return idadePaciente;
    }
    public void setIdadePaciente(int idadePaciente) {
        this.idadePaciente = idadePaciente;
    }
    public String getCPFPaciente() {
        return CPFPaciente;
    }
    public void setCPFPaciente(String cPFPaciente) {
        CPFPaciente = cPFPaciente;
    }
    public String getEnderecoPaciente() {
        return enderecoPaciente;
    }
    public void setEnderecoPaciente(String enderecoPaciente) {
        this.enderecoPaciente = enderecoPaciente;
    }
    public String getTelefonePaciente() {
        return telefonePaciente;
    }
    public void setTelefonePaciente(String telefonePaciente) {
        this.telefonePaciente = telefonePaciente;
    }
    public String getTipoSanguineoPaciente() {
        return tipoSanguineoPaciente;
    }
    public void setTipoSanguineoPaciente(String tipoSanguineoPaciente) {
        this.tipoSanguineoPaciente = tipoSanguineoPaciente;
    }
}
