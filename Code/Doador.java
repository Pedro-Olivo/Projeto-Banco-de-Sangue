public class Doador {
    
    protected String nomeDoador;
    protected int idadeDoador;
    protected String CPFDoador;
    protected String enderecoDoador;
    protected String telefoneDoador;
    protected String tipoSanguineoDoador;

    
    public String getNomeDoador() {
        return nomeDoador;
    }
    public void setNomeDoador(String nomeDoador) {
        this.nomeDoador = nomeDoador;
    }
    public int getIdadeDoador() {
        return idadeDoador;
    }
    public void setIdadeDoador(int idadeDoador) {
        this.idadeDoador = idadeDoador;
    }
    public String getCPFDoador() {
        return CPFDoador;
    }
    public void setCPFDoador(String cPFDoador) {
        CPFDoador = cPFDoador;
    }
    public String getEnderecoDoador() {
        return enderecoDoador;
    }
    public void setEnderecoDoador(String enderecoDoador) {
        this.enderecoDoador = enderecoDoador;
    }
    public String getTelefoneDoador() {
        return telefoneDoador;
    }
    public void setTelefoneDoador(String telefoneDoador) {
        this.telefoneDoador = telefoneDoador;
    }
    public String getTipoSanguineoDoador() {
        return tipoSanguineoDoador;
    }
    public void setTipoSanguineoDoador(String tipoSanguineoDoador) {
        this.tipoSanguineoDoador = tipoSanguineoDoador;
    }
}
